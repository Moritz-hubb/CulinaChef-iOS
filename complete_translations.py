def translate_value(value, lang):
    """Translate a value if it starts with [DE]"""
    if not value.startswith("[DE] "):
        return value
    
    german_text = value[5:]  # Remove "[DE] " prefix
    
    # Try to find translation
    if german_text in TRANSLATIONS.get(lang, {}):
        return TRANSLATIONS[lang][german_text]
    
    # Keep original if no translation found
    return value

def process_file(filepath, lang):
    """Process a single localization file"""
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    translated_count = 0
    remaining_count = 0
    
    for key, value in data.items():
        if value.startswith("[DE] "):
            new_value = translate_value(value, lang)
            if new_value != value:
                data[key] = new_value
                translated_count += 1
            else:
                remaining_count += 1
    
    # Save with sorted keys
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write('\n')
    
    return translated_count, remaining_count

# Process all language files
for lang in ["fr", "it", "es"]:
    filepath = LOCALIZATION_DIR / f"{lang}.json"
    if filepath.exists():
        translated, remaining = process_file(filepath, lang)
        print(f"✅ {lang}.json: Translated {translated} keys, {remaining} still need translation")
    else:
        print(f"⚠️  {lang}.json not found")

